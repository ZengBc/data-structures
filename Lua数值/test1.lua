--关系运算符
--lua语言提供了下列关系运算
--< > <= >= == ~=
--这些关系运算的结果是Boolean类型
--==用于相等性测试 ~=用于不等性测试
--这两个运算符可以应用于任意两个值
--当这两个值的类型不同时，Lua语言认为它们是不相等的
--否则，会根据它们的类型再对两者进行比较
--比较数值时应永远忽略数值的子类型数值究竟是以整型还是浮点型表示并无区别
--只与算术值有关(尽管如此 比较具有相同子类型的数值时效率更高)
--数学库
--Lua语言提供了标准的数学库math
--标准数学库由一组标准的数学函数组成 包括三角函数(sin cos tan asin等)
--指数函数 取整函数 最大和最小函数max和min 用于生成伪随机数的random
--以及常量pi 和huge 最大可表示数值 在大多数平台上代表inf
--所有的三角函数都以弧度为单位 并通过函数deg和rad进行角度和弧度的转换

